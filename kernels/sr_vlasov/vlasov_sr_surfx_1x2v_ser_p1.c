{ 
  // w[NDIM]:   Cell-center coordinates.
  // dxv[NDIM]: Cell spacing.
  // p_over_gamma: p/gamma (velocity).
  // fl/fc/fr:  Input Distribution function in left/center/right cells.
  // out:       Incremented distribution function in center cell.
  const double *p0_over_gamma = &p_over_gamma[0]; 
  double alpha[4] = {0.0}; 
  alpha[0] = p0_over_gamma[0]; 
  alpha[1] = p0_over_gamma[1]; 
  alpha[2] = p0_over_gamma[2]; 
  alpha[3] = p0_over_gamma[3]; 

  const double dx10 = 2/dxv[0]; 
  const double dv = dxv[1], wv = w[1]; 
  double Ghat_r[4]; 
  double Ghat_l[4]; 
  if (wv>0) { 

  Ghat_r[0] = alpha[3]*(0.6123724356957944*fc[7]+0.3535533905932737*fc[6])+0.6123724356957944*(alpha[2]*fc[5]+alpha[1]*fc[4])+0.3535533905932737*(alpha[2]*fc[3]+alpha[1]*fc[2])+alpha[0]*(0.6123724356957944*fc[1]+0.3535533905932737*fc[0]); 
  Ghat_r[1] = alpha[2]*(0.6123724356957944*fc[7]+0.3535533905932737*fc[6])+0.6123724356957944*(alpha[3]*fc[5]+alpha[0]*fc[4])+0.3535533905932737*(alpha[3]*fc[3]+alpha[0]*fc[2])+alpha[1]*(0.6123724356957944*fc[1]+0.3535533905932737*fc[0]); 
  Ghat_r[2] = alpha[1]*(0.6123724356957944*fc[7]+0.3535533905932737*fc[6])+0.6123724356957944*(alpha[0]*fc[5]+alpha[3]*fc[4])+0.3535533905932737*(alpha[0]*fc[3]+fc[2]*alpha[3])+(0.6123724356957944*fc[1]+0.3535533905932737*fc[0])*alpha[2]; 
  Ghat_r[3] = alpha[0]*(0.6123724356957944*fc[7]+0.3535533905932737*fc[6])+0.6123724356957944*(alpha[1]*fc[5]+alpha[2]*fc[4])+0.3535533905932737*alpha[1]*fc[3]+0.6123724356957944*fc[1]*alpha[3]+0.3535533905932737*(fc[0]*alpha[3]+alpha[2]*fc[2]); 

  Ghat_l[0] = alpha[3]*(0.6123724356957944*fl[7]+0.3535533905932737*fl[6])+0.6123724356957944*(alpha[2]*fl[5]+alpha[1]*fl[4])+0.3535533905932737*(alpha[2]*fl[3]+alpha[1]*fl[2])+alpha[0]*(0.6123724356957944*fl[1]+0.3535533905932737*fl[0]); 
  Ghat_l[1] = alpha[2]*(0.6123724356957944*fl[7]+0.3535533905932737*fl[6])+0.6123724356957944*(alpha[3]*fl[5]+alpha[0]*fl[4])+0.3535533905932737*(alpha[3]*fl[3]+alpha[0]*fl[2])+alpha[1]*(0.6123724356957944*fl[1]+0.3535533905932737*fl[0]); 
  Ghat_l[2] = alpha[1]*(0.6123724356957944*fl[7]+0.3535533905932737*fl[6])+0.6123724356957944*(alpha[0]*fl[5]+alpha[3]*fl[4])+0.3535533905932737*(alpha[0]*fl[3]+fl[2]*alpha[3])+(0.6123724356957944*fl[1]+0.3535533905932737*fl[0])*alpha[2]; 
  Ghat_l[3] = alpha[0]*(0.6123724356957944*fl[7]+0.3535533905932737*fl[6])+0.6123724356957944*(alpha[1]*fl[5]+alpha[2]*fl[4])+0.3535533905932737*alpha[1]*fl[3]+0.6123724356957944*fl[1]*alpha[3]+0.3535533905932737*(fl[0]*alpha[3]+alpha[2]*fl[2]); 

  } else { 

  Ghat_r[0] = -0.25*(alpha[3]*(2.449489742783178*fr[7]-1.414213562373095*fr[6])+2.449489742783178*(alpha[2]*fr[5]+alpha[1]*fr[4])-1.414213562373095*(alpha[2]*fr[3]+alpha[1]*fr[2])+alpha[0]*(2.449489742783178*fr[1]-1.414213562373095*fr[0])); 
  Ghat_r[1] = -0.25*(alpha[2]*(2.449489742783178*fr[7]-1.414213562373095*fr[6])+2.449489742783178*(alpha[3]*fr[5]+alpha[0]*fr[4])-1.414213562373095*(alpha[3]*fr[3]+alpha[0]*fr[2])+alpha[1]*(2.449489742783178*fr[1]-1.414213562373095*fr[0])); 
  Ghat_r[2] = -0.25*(alpha[1]*(2.449489742783178*fr[7]-1.414213562373095*fr[6])+2.449489742783178*(alpha[0]*fr[5]+alpha[3]*fr[4])-1.414213562373095*(alpha[0]*fr[3]+fr[2]*alpha[3])+(2.449489742783178*fr[1]-1.414213562373095*fr[0])*alpha[2]); 
  Ghat_r[3] = -0.25*(alpha[0]*(2.449489742783178*fr[7]-1.414213562373095*fr[6])+2.449489742783178*(alpha[1]*fr[5]+alpha[2]*fr[4])-1.414213562373095*alpha[1]*fr[3]+(2.449489742783178*fr[1]-1.414213562373095*fr[0])*alpha[3]-1.414213562373095*alpha[2]*fr[2]); 

  Ghat_l[0] = -0.25*(alpha[3]*(2.449489742783178*fc[7]-1.414213562373095*fc[6])+2.449489742783178*(alpha[2]*fc[5]+alpha[1]*fc[4])-1.414213562373095*(alpha[2]*fc[3]+alpha[1]*fc[2])+alpha[0]*(2.449489742783178*fc[1]-1.414213562373095*fc[0])); 
  Ghat_l[1] = -0.25*(alpha[2]*(2.449489742783178*fc[7]-1.414213562373095*fc[6])+2.449489742783178*(alpha[3]*fc[5]+alpha[0]*fc[4])-1.414213562373095*(alpha[3]*fc[3]+alpha[0]*fc[2])+alpha[1]*(2.449489742783178*fc[1]-1.414213562373095*fc[0])); 
  Ghat_l[2] = -0.25*(alpha[1]*(2.449489742783178*fc[7]-1.414213562373095*fc[6])+2.449489742783178*(alpha[0]*fc[5]+alpha[3]*fc[4])-1.414213562373095*(alpha[0]*fc[3]+fc[2]*alpha[3])+(2.449489742783178*fc[1]-1.414213562373095*fc[0])*alpha[2]); 
  Ghat_l[3] = -0.25*(alpha[0]*(2.449489742783178*fc[7]-1.414213562373095*fc[6])+2.449489742783178*(alpha[1]*fc[5]+alpha[2]*fc[4])-1.414213562373095*alpha[1]*fc[3]+(2.449489742783178*fc[1]-1.414213562373095*fc[0])*alpha[3]-1.414213562373095*alpha[2]*fc[2]); 

  } 
  out[0] += (0.7071067811865475*Ghat_l[0]-0.7071067811865475*Ghat_r[0])*dx10; 
  out[1] += -1.224744871391589*(Ghat_r[0]+Ghat_l[0])*dx10; 
  out[2] += (0.7071067811865475*Ghat_l[1]-0.7071067811865475*Ghat_r[1])*dx10; 
  out[3] += (0.7071067811865475*Ghat_l[2]-0.7071067811865475*Ghat_r[2])*dx10; 
  out[4] += -1.224744871391589*(Ghat_r[1]+Ghat_l[1])*dx10; 
  out[5] += -1.224744871391589*(Ghat_r[2]+Ghat_l[2])*dx10; 
  out[6] += (0.7071067811865475*Ghat_l[3]-0.7071067811865475*Ghat_r[3])*dx10; 
  out[7] += -1.224744871391589*(Ghat_r[3]+Ghat_l[3])*dx10; 

  return 0.;

} 
